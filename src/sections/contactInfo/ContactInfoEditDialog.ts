type ContactInfoFormState = {
  emails: ContactPointRow[]
  phones: ContactPointRow[]
  addresses: ContactAddressRow[]
}

function isContactPointRow(row: ContactPointRow | ContactAddressRow): row is ContactPointRow {
  return 'value' in row
}

function rowHasContent(row: ContactPointRow | ContactAddressRow): boolean {
  if (isContactPointRow(row)) {
    return hasNonEmptyText(row.value)
  }

  return [
    row.streetAddress,
    row.locality,
    row.region,
    row.postalCode,
    row.countryName
  ].some(hasNonEmptyText)
}

function toFormState(contactInfo: ContactInfo): ContactInfoFormState {
  const emails = (contactInfo.emails || [])
    .map((email) => ({
      value: sanitizeEmailValue(toText(email.valueNode).replace(/^mailto:/i, '')),
      type: normalizeEmailTypeForContactInfoEdit(email.type),
      entryNode: toText(email.entryNode),
      status: toText(email.entryNode) ? 'existing' as const : 'new' as const
    }))
    .filter((email) => Boolean(email.value || email.entryNode || email.type))

  const phones = (contactInfo.phones || [])
    .map((phone) => ({
      value: sanitizeTextValue(toText(phone.valueNode).replace(/^tel:/i, '')),
      type: normalizePhoneTypeForContactInfoEdit(phone.type),
      entryNode: toText(phone.entryNode),
      status: 'existing' as const
    }))
    .filter((phone) => Boolean(phone.value))
  const addresses = (contactInfo.addresses || [])
    .map((address) => ({
      streetAddress: sanitizeAddressFieldValue(toText(address.streetAddress)),
      locality: sanitizeAddressFieldValue(toText(address.locality)),
      region: sanitizeAddressFieldValue(toText(address.region)),
      postalCode: sanitizeAddressFieldValue(toText(address.postalCode)),
      countryName: sanitizeAddressFieldValue(toText(address.countryName)),
      type: toText(address.type),
      entryNode: toText(address.entryNode),
      status: toText(address.entryNode) ? 'existing' as const : 'new' as const
    }))
    .filter((address) => rowHasContent(address) || Boolean(address.entryNode || address.type))

  return {
    emails: emails.length ? emails : [{ value: '', type: '', entryNode: '', status: 'new' }],
    phones: phones.length ? phones : [{ value: '', type: '', entryNode: '', status: 'new' }],
    addresses: addresses.length
      ? addresses
      : [{ streetAddress: '', locality: '', region: '', postalCode: '', countryName: '', type: '', entryNode: '', status: 'new' }]
  }
}

