import { RowStatus } from "./types"

export type EditableRow = {
  entryNode: string
  status: RowStatus
}

export type TypedEditableRow = EditableRow & {
  type: string
}

type StringKeyOf<T> = {
  [K in keyof T]-?: T[K] extends string ? K : never
}[keyof T]

export function deleteRow<T extends EditableRow>(rows: T[], index: number) {
  const row = rows[index]
  if (!row) return

  if (row.entryNode) {
    row.status = 'deleted'
    return
  }

  rows.splice(index, 1)
}

export function applyRowSelectChange<T extends EditableRow, K extends StringKeyOf<T>>(
  row: T,
  field: K,
  nextValue: Extract<T[K], string>
) {
  row[field] = nextValue.trim() as T[K]

  if (row.entryNode) {
    row.status = row.status === 'existing' || row.status === 'deleted' ? 'modified' : row.status
    return
  }

  row.status = 'new'
}

export function summarizeRowOps<T extends EditableRow>(rows: T[], rowHasContent: (row: T) => boolean) {
  const create = rows.filter((row) => row.status === 'new' && rowHasContent(row))
  const update = rows.filter((row) => row.status === 'modified' && rowHasContent(row))
  const remove = rows.filter((row) => row.status === 'deleted')
  return { create, update, remove }
}

