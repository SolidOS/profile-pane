export function applyRowFieldChange<T extends EditableRow, K extends keyof T>(
  row: T,
  field: K,
  nextValue: T[K],
  hasContent: (row: T) => boolean
) {
  row[field] = nextValue

  if (!hasContent(row)) {
    row.status = row.entryNode ? 'deleted' : 'new'
    return
  }

  if (row.entryNode) {
    row.status = row.status === 'existing' || row.status === 'deleted' ? 'modified' : row.status
    return
  }

  row.status = 'new'
}
